<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
    <h5>{{ counter }}</h5>
    <button @click="increment()">increment</button>
  </div>
</template>

<script>
export default {
  name: 'HelloWorld',
  data() {
    return {
      counter: 0
    }
  }, 
  methods: {
    increment() {
      this.counter++
    }
  },
  props: {
    msg: String
  }
}
</script>

